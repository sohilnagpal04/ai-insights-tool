# ── Generic CSV sections ──────────────────────────────────────────────────────

def _pdf_generic(story, S, summary, insights):
    missing_total = sum(summary.get("missing_values", {}).values())
    story.append(_kpi_row([
        ("Rows",           str(summary.get("row_count", "—"))),
        ("Columns",        str(summary.get("column_count", "—"))),
        ("Anomalies",      str(summary.get("anomaly_count", 0))),
        ("Missing Values", str(missing_total)),
    ]))
    story.append(Spacer(1, 10))

    if insights:
        _section(story, S, "AI-Generated Insights")
        if insights.get("executive_summary"):
            story.append(Paragraph("Executive Summary", S["label"]))
            story.append(Paragraph(insights["executive_summary"], S["body"]))
            story.append(Spacer(1, 6))
        if insights.get("insights"):
            story.append(Paragraph("Key Insights", S["label"]))
            for ins in insights["insights"]:
                story.append(Paragraph(f"• <b>{ins['title']}</b>: {ins['detail']}", S["body"]))
            story.append(Spacer(1, 6))
        if insights.get("recommendation"):
            story.append(Paragraph("Recommendation", S["label"]))
            story.append(_highlight_box(insights["recommendation"], colors.HexColor("#ecfeff"), CYAN, S))
            story.append(Spacer(1, 6))
        if insights.get("anomalies"):
            story.append(Paragraph("Anomaly Analysis", S["label"]))
            story.append(_highlight_box(insights["anomalies"], colors.HexColor("#fffbeb"), AMBER, S))

    if summary.get("numeric_summary"):
        _section(story, S, "Numeric Summary")
        ns = summary["numeric_summary"]
        stats = ["mean", "std", "min", "50%", "max"]
        cols  = list((ns.get("mean") or {}).keys())
        if cols:
            header = ["Column"] + [s.upper() for s in stats]
            rows   = [header]
            for col in cols:
                row = [col]
                for s in stats:
                    v = (ns.get(s) or {}).get(col)
                    row.append(f"{v:.2f}" if v is not None else "—")
                rows.append(row)
            stat_col_w = (PAGE_W - 55 * mm) / len(stats)
            t = Table(rows, colWidths=[55 * mm] + [stat_col_w] * len(stats))
            t.setStyle(TableStyle([
                ("BACKGROUND",    (0, 0), (-1, 0), DARK),
                ("TEXTCOLOR",     (0, 0), (-1, 0), WHITE),
                ("FONTNAME",      (0, 0), (-1, -1), "Helvetica"),
                ("FONTNAME",      (0, 0), (-1, 0), "Helvetica-Bold"),
                ("FONTSIZE",      (0, 0), (-1, -1), 8),
                ("ALIGN",         (1, 0), (-1, -1), "RIGHT"),
                ("ROWBACKGROUNDS",(0, 1), (-1, -1), [WHITE, LIGHT]),
                ("TEXTCOLOR",     (0, 1), (-1, -1), SLATE),
                ("GRID",          (0, 0), (-1, -1), 0.4, colors.HexColor("#e2e8f0")),
                ("TOPPADDING",    (0, 0), (-1, -1), 5),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 5),
            ]))
            story.append(t)

    if summary.get("top_correlations"):
        _section(story, S, "Top Correlations")
        corr_data = [["Column Pair", "Correlation"]] + [
            [c["columns"], f"{c['correlation']:.3f}"] for c in summary["top_correlations"]
        ]
        t = Table(corr_data, colWidths=[PAGE_W - 44 * mm, 44 * mm])
        t.setStyle(TableStyle([
            ("BACKGROUND",    (0, 0), (-1, 0), DARK),
            ("TEXTCOLOR",     (0, 0), (-1, 0), WHITE),
            ("FONTNAME",      (0, 0), (-1, 0), "Helvetica-Bold"),
            ("FONTNAME",      (0, 1), (-1, -1), "Helvetica"),
            ("FONTSIZE",      (0, 0), (-1, -1), 9),
            ("ALIGN",         (1, 0), (-1, -1), "CENTER"),
            ("ROWBACKGROUNDS",(0, 1), (-1, -1), [WHITE, LIGHT]),
            ("TEXTCOLOR",     (0, 1), (-1, -1), SLATE),
            ("GRID",          (0, 0), (-1, -1), 0.4, colors.HexColor("#e2e8f0")),
            ("TOPPADDING",    (0, 0), (-1, -1), 6),
            ("BOTTOMPADDING", (0, 0), (-1, -1), 6),
        ]))
        story.append(t)

    anomaly_samples = summary.get("anomaly_sample", [])
    if anomaly_samples:
        _section(story, S, "Anomalous Row Samples", AMBER)
        story.append(Paragraph(
            f"{summary.get('anomaly_count', 0)} anomalous rows detected (showing first 5).",
            S["body"]
        ))
        cols = list(anomaly_samples[0].keys())
        col_w = PAGE_W / len(cols)
        hdr_s  = ParagraphStyle("ah", fontSize=7, textColor=DARK, fontName="Helvetica-Bold", leading=9)
        cell_s = ParagraphStyle("ac", fontSize=7, textColor=SLATE, fontName="Helvetica", leading=9)
        rows   = [[Paragraph(c, hdr_s) for c in cols]] + [
            [Paragraph(str(r.get(c, "—")), cell_s) for c in cols] for r in anomaly_samples
        ]
        t = Table(rows, colWidths=[col_w] * len(cols))
        t.setStyle(TableStyle([
            ("BACKGROUND",    (0, 0), (-1, 0), AMBER),
            ("FONTSIZE",      (0, 0), (-1, -1), 7),
            ("ROWBACKGROUNDS",(0, 1), (-1, -1), [WHITE, colors.HexColor("#fffbeb")]),
            ("GRID",          (0, 0), (-1, -1), 0.4, colors.HexColor("#fde68a")),
            ("TOPPADDING",    (0, 0), (-1, -1), 4),
            ("BOTTOMPADDING", (0, 0), (-1, -1), 4),
            ("VALIGN",        (0, 0), (-1, -1), "TOP"),
        ]))
        story.append(t)


# ── Public entry point ────────────────────────────────────────────────────────

def generate_pdf(filename: str, summary: dict, insights: dict | None, hubspot: dict | None = None) -> bytes:
    buf = io.BytesIO()
    doc = SimpleDocTemplate(buf, pagesize=A4,
                            leftMargin=18*mm, rightMargin=18*mm,
                            topMargin=18*mm, bottomMargin=18*mm)
    S     = _styles()
    story = []

    source = (hubspot or {}).get("source_type", "csv")

    # Title varies by source
    titles = {
        "hubspot_deals":     ("Sales Pipeline Report",    f"HubSpot Deals · {filename}"),
        "hubspot_contacts":  ("Contact Intelligence Report", f"HubSpot Contacts · {filename}"),
        "hubspot_companies": ("Company Intelligence Report",  f"HubSpot Companies · {filename}"),
    }
    title, subtitle = titles.get(source, ("AI Insights Report", f"Dataset: {filename}"))

    _header(story, S, title, subtitle, filename)

    if source == "hubspot_deals":
        _pdf_deals(story, S, hubspot)
    elif source == "hubspot_contacts":
        _pdf_contacts(story, S, hubspot)
    elif source == "hubspot_companies":
        _pdf_companies(story, S, hubspot)
    else:
        _pdf_generic(story, S, summary, insights)

    # Always append AI insights for HubSpot sources too
    if hubspot and insights:
        _section(story, S, "AI Analysis")
        if insights.get("executive_summary"):
            story.append(Paragraph(insights["executive_summary"], S["body"]))
            story.append(Spacer(1, 6))
        if insights.get("recommendation"):
            story.append(_highlight_box(insights["recommendation"], colors.HexColor("#ecfeff"), CYAN, S))

    _footer(story)
    doc.build(story)
    return buf.getvalue()
