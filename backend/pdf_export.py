import io
from datetime import datetime

from reportlab.lib import colors
from reportlab.lib.pagesizes import A4
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import mm
from reportlab.platypus import (
    SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle, HRFlowable
)
from reportlab.lib.enums import TA_CENTER

PAGE_W = A4[0] - 36 * mm

CYAN    = colors.HexColor("#06b6d4")
SLATE   = colors.HexColor("#334155")
LIGHT   = colors.HexColor("#f1f5f9")
WHITE   = colors.white
AMBER   = colors.HexColor("#f59e0b")
EMERALD = colors.HexColor("#10b981")
ROSE    = colors.HexColor("#f43f5e")
VIOLET  = colors.HexColor("#8b5cf6")
DARK    = colors.HexColor("#0f172a")
ORANGE  = colors.HexColor("#f97316")


def _styles():
    return {
        "title":    ParagraphStyle("title",    fontSize=22, textColor=DARK,  leading=28, spaceAfter=0, fontName="Helvetica-Bold"),
        "subtitle": ParagraphStyle("subtitle", fontSize=11, textColor=SLATE, leading=14, spaceAfter=0, fontName="Helvetica"),
        "section":  ParagraphStyle("section",  fontSize=13, textColor=DARK,  spaceBefore=14, spaceAfter=6, fontName="Helvetica-Bold"),
        "body":     ParagraphStyle("body",     fontSize=10, textColor=SLATE, leading=15, spaceAfter=4, fontName="Helvetica"),
        "label":    ParagraphStyle("label",    fontSize=8,  textColor=colors.HexColor("#94a3b8"), fontName="Helvetica-Bold", spaceAfter=2),
        "small":    ParagraphStyle("small",    fontSize=8,  textColor=SLATE, leading=11, fontName="Helvetica"),
    }


def _header(story, S, title, subtitle, filename):
    t = Table(
        [
            [Paragraph(title, S["title"])],
            [Paragraph(subtitle, S["subtitle"])],
            [Paragraph(f"Generated: {datetime.now().strftime('%d %B %Y, %H:%M')}", S["subtitle"])],
        ],
        colWidths=[PAGE_W], rowHeights=[36, 18, 18],
    )
    t.setStyle(TableStyle([
        ("TOPPADDING",    (0, 0), (-1, -1), 0),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 0),
        ("LEFTPADDING",   (0, 0), (-1, -1), 0),
        ("RIGHTPADDING",  (0, 0), (-1, -1), 0),
        ("VALIGN",        (0, 0), (-1, -1), "BOTTOM"),
    ]))
    story.append(t)
    story.append(Spacer(1, 8))
    story.append(HRFlowable(width="100%", thickness=1, color=CYAN, spaceAfter=10))


def _footer(story):
    story.append(Spacer(1, 16))
    story.append(HRFlowable(width="100%", thickness=0.5, color=colors.HexColor("#e2e8f0")))
    story.append(Paragraph(
        "Generated by <b>AI Insights Tool</b> — sohilnagpal.com",
        ParagraphStyle("footer", fontSize=8, textColor=colors.HexColor("#94a3b8"),
                       alignment=TA_CENTER, spaceBefore=4)
    ))


def _kpi_row(stats):
    """Cyan-header stat chips: [(label, value), ...]"""
    data  = [[v for _, v in stats], [l for l, _ in stats]]
    col_w = PAGE_W / len(stats)
    t = Table(data, colWidths=[col_w] * len(stats))
    t.setStyle(TableStyle([
        ("BACKGROUND",    (0, 0), (-1, 0), CYAN),
        ("TEXTCOLOR",     (0, 0), (-1, 0), WHITE),
        ("FONTNAME",      (0, 0), (-1, 0), "Helvetica-Bold"),
        ("FONTSIZE",      (0, 0), (-1, 0), 15),
        ("ALIGN",         (0, 0), (-1, -1), "CENTER"),
        ("VALIGN",        (0, 0), (-1, -1), "MIDDLE"),
        ("ROWBACKGROUNDS",(0, 1), (-1, 1), [LIGHT]),
        ("TEXTCOLOR",     (0, 1), (-1, 1), SLATE),
        ("FONTSIZE",      (0, 1), (-1, 1), 8),
        ("FONTNAME",      (0, 1), (-1, 1), "Helvetica"),
        ("TOPPADDING",    (0, 0), (-1, -1), 8),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 8),
        ("GRID",          (0, 0), (-1, -1), 0.5, colors.HexColor("#e2e8f0")),
    ]))
    return t


def _fmt_money(n):
    if n is None: return "—"
    try:
        n = float(n)
        if n >= 1e6: return f"${n/1e6:.1f}M"
        if n >= 1e3: return f"${n/1e3:.0f}K"
        return f"${n:.0f}"
    except Exception:
        return str(n)


def _section(story, S, title, color=None):
    story.append(Paragraph(title, S["section"]))
    story.append(HRFlowable(width="100%", thickness=0.5, color=color or SLATE, spaceAfter=6))


def _highlight_box(text, bg, border, S):
    t = Table(
        [[Paragraph(text, ParagraphStyle("hb", fontSize=10, textColor=DARK, leading=15, fontName="Helvetica"))]],
        colWidths=[PAGE_W],
    )
    t.setStyle(TableStyle([
        ("BACKGROUND",    (0, 0), (-1, -1), bg),
        ("BOX",           (0, 0), (-1, -1), 1.5, border),
        ("TOPPADDING",    (0, 0), (-1, -1), 8),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 8),
        ("LEFTPADDING",   (0, 0), (-1, -1), 10),
    ]))
    return t


# ── HubSpot Deals PDF ─────────────────────────────────────────────────────────

def _pdf_deals(story, S, hs):
    _section(story, S, "Pipeline Summary", CYAN)
    story.append(_kpi_row([
        ("Total Pipeline",  _fmt_money(hs.get("total_pipeline"))),
        ("Weighted Value",  _fmt_money(hs.get("weighted_pipeline"))),
        ("Avg Deal Size",   _fmt_money(hs.get("avg_deal_size"))),
        ("Win Rate",        f"{hs.get('win_rate', 0)}%"),
        ("Open Deals",      str(hs.get("open_deals", 0))),
    ]))
    story.append(Spacer(1, 10))

    # Won / Lost
    won  = hs.get("won_deals", 0)
    lost = hs.get("lost_deals", 0)
    wl = Table(
        [["Closed Won", "Closed Lost"], [str(won), str(lost)]],
        colWidths=[PAGE_W / 2, PAGE_W / 2],
    )
    wl.setStyle(TableStyle([
        ("BACKGROUND",    (0, 0), (0, 0), EMERALD),
        ("BACKGROUND",    (1, 0), (1, 0), ROSE),
        ("TEXTCOLOR",     (0, 0), (-1, 0), WHITE),
        ("FONTNAME",      (0, 0), (-1, -1), "Helvetica-Bold"),
        ("FONTSIZE",      (0, 0), (-1, 0), 9),
        ("FONTSIZE",      (0, 1), (-1, 1), 18),
        ("ALIGN",         (0, 0), (-1, -1), "CENTER"),
        ("VALIGN",        (0, 0), (-1, -1), "MIDDLE"),
        ("ROWBACKGROUNDS",(0, 1), (-1, 1), [colors.HexColor("#ecfdf5"), colors.HexColor("#fff1f2")]),
        ("TEXTCOLOR",     (0, 1), (0, 1), EMERALD),
        ("TEXTCOLOR",     (1, 1), (1, 1), ROSE),
        ("TOPPADDING",    (0, 0), (-1, -1), 8),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 8),
        ("GRID",          (0, 0), (-1, -1), 0.5, colors.HexColor("#e2e8f0")),
    ]))
    story.append(wl)
    story.append(Spacer(1, 10))

    # Pipeline by stage table
    pipeline = [s for s in (hs.get("pipeline_by_stage") or []) if s.get("stage") != "closedlost"]
    if pipeline:
        _section(story, S, "Pipeline by Stage", VIOLET)
        rows = [["Stage", "Deals", "Value"]] + [
            [s["label"], str(s["count"]), _fmt_money(s["value"])]
            for s in pipeline
        ]
        t = Table(rows, colWidths=[PAGE_W * 0.5, PAGE_W * 0.2, PAGE_W * 0.3])
        t.setStyle(TableStyle([
            ("BACKGROUND",    (0, 0), (-1, 0), DARK),
            ("TEXTCOLOR",     (0, 0), (-1, 0), WHITE),
            ("FONTNAME",      (0, 0), (-1, 0), "Helvetica-Bold"),
            ("FONTNAME",      (0, 1), (-1, -1), "Helvetica"),
            ("FONTSIZE",      (0, 0), (-1, -1), 9),
            ("ALIGN",         (1, 0), (-1, -1), "RIGHT"),
            ("ROWBACKGROUNDS",(0, 1), (-1, -1), [WHITE, LIGHT]),
            ("TEXTCOLOR",     (0, 1), (-1, -1), SLATE),
            ("GRID",          (0, 0), (-1, -1), 0.4, colors.HexColor("#e2e8f0")),
            ("TOPPADDING",    (0, 0), (-1, -1), 6),
            ("BOTTOMPADDING", (0, 0), (-1, -1), 6),
        ]))
        story.append(t)
        story.append(Spacer(1, 10))

    # At-risk deals
    at_risk = hs.get("at_risk_deals") or []
    if at_risk:
        _section(story, S, "Deals Needing Attention", AMBER)
        rows = [["Deal Name", "Stage", "Value", "Risk"]] + [
            [
                Paragraph(d.get("name", "—"), S["small"]),
                d.get("stage", "—"),
                _fmt_money(d.get("amount")),
                f"{d['days_overdue']}d overdue" if d.get("days_overdue") is not None else "No close date",
            ]
            for d in at_risk
        ]
        cw = [PAGE_W * 0.38, PAGE_W * 0.22, PAGE_W * 0.18, PAGE_W * 0.22]
        t = Table(rows, colWidths=cw)
        t.setStyle(TableStyle([
            ("BACKGROUND",    (0, 0), (-1, 0), AMBER),
            ("TEXTCOLOR",     (0, 0), (-1, 0), DARK),
            ("FONTNAME",      (0, 0), (-1, 0), "Helvetica-Bold"),
            ("FONTNAME",      (0, 1), (-1, -1), "Helvetica"),
            ("FONTSIZE",      (0, 0), (-1, -1), 8),
            ("ROWBACKGROUNDS",(0, 1), (-1, -1), [WHITE, colors.HexColor("#fffbeb")]),
            ("TEXTCOLOR",     (0, 1), (-1, -1), SLATE),
            ("GRID",          (0, 0), (-1, -1), 0.4, colors.HexColor("#fde68a")),
            ("TOPPADDING",    (0, 0), (-1, -1), 5),
            ("BOTTOMPADDING", (0, 0), (-1, -1), 5),
            ("VALIGN",        (0, 0), (-1, -1), "TOP"),
        ]))
        story.append(t)


